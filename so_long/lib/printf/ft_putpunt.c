#include"ft_printf.h"

int	ft_putpunt(size_t nb, int *len)
{
	if (nb >= 16)
		ft_putpunt((nb / 16), len);
	return (ft_putchar("0123456789abcdef"[nb % 16]), *len += 1);
}
